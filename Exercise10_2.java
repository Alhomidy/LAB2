public class Exercise10_2 {
    public static void main(String[] args) {
        int a=20,b=25;
        System.out.println("The sum is "+(a+b));
        System.out.println("The sum is "+(a-b));
        System.out.println("The sum is "+(a*b));
        System.out.println("The sum is "+(a+b)/2.0);
        System.out.println("The sum is "+Math.abs(a-b));
        System.out.println("The sum is "+Math.max(a,b));
        System.out.println("The sum is "+Math.min(a,b));
    }
}
